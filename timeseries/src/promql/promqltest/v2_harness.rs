//! v2 harness for the `promqltest` corpus.
//!
//! This mirrors [`super::runner::run_test`] but routes evaluation through
//! `Tsdb::eval_query_v2` instead of `Tsdb::eval_query`. It exists so RFC 0007
//! Phase 6 can run the unmodified `.test` fixtures against the v2 engine and
//! triage failures without touching the v1 harness.
//!
//! Intentionally duplicates the runner rather than parameterising it: the v1
//! path is the correctness oracle for other suites that depend on the existing
//! harness shape, and keeping the v2 router in a separate file makes the diff
//! easy to review and the v1 code path unchanged.
//!
//! **Do not edit fixtures under `testdata/`** — RFC 0007 Phase 6 hard rule §1.4.

#![cfg(feature = "promql-v2")]

use crate::model::{QueryOptions, RangeSample};
use crate::promql::promqltest::assert::assert_results;
use crate::promql::promqltest::dsl::*;
use crate::promql::promqltest::loader::load_series;
use crate::storage::merge_operator::OpenTsdbMergeOperator;
use crate::tsdb::Tsdb;
use common::storage::in_memory::InMemoryStorage;
use std::path::{Path, PathBuf};
use std::sync::Arc;
use std::time::SystemTime;

/// v2 analogue of [`super::evaluator::eval_instant`]. Routes through
/// `Tsdb::eval_query_v2` and coerces the `QueryValue` to `Vec<RangeSample>`
/// so it is directly comparable to the v1 path via [`assert_results`].
async fn eval_instant_v2(
    tsdb: &Tsdb,
    time: SystemTime,
    query: &str,
) -> Result<Vec<RangeSample>, String> {
    tsdb.eval_query_v2(query, Some(time), &QueryOptions::default())
        .await
        .map_err(|e| e.to_string())
        .map(|qv| qv.into_matrix())
}

fn new_test_storage() -> Tsdb {
    let storage = Arc::new(InMemoryStorage::with_merge_operator(Arc::new(
        OpenTsdbMergeOperator,
    )));
    Tsdb::new(storage)
}

/// Run a single `.test` fixture through the v2 engine. Structure mirrors
/// [`super::runner::run_test`] with one deliberate difference: eval-level
/// failures are **collected**, not short-circuited, so a single corpus run
/// surfaces every failure per file rather than stopping at the first.
/// `Load`/`Clear`/`parse_test_file` errors remain fatal because they break
/// the remaining eval invariants.
pub async fn run_test_v2(name: &str, content: &str) -> Result<(), String> {
    let commands = parse_test_file(content)?;
    let mut tsdb = new_test_storage();
    let mut eval_count = 0;
    let mut ignoring = false;
    let mut failures: Vec<String> = Vec::new();

    for cmd in commands {
        match cmd {
            Command::Clear(_) => {
                tsdb = new_test_storage();
            }
            Command::Ignore(_) => {
                ignoring = true;
            }
            Command::Resume(_) => {
                ignoring = false;
            }
            Command::Load(load_cmd) => {
                if !ignoring {
                    load_series(&tsdb, load_cmd.interval, &load_cmd.series).await?;
                }
            }
            Command::EvalInstant(eval_cmd) => {
                if !ignoring {
                    eval_count += 1;
                    match eval_instant_v2(&tsdb, eval_cmd.time, &eval_cmd.query).await {
                        Ok(result) => {
                            if let Err(e) = assert_results(
                                &result,
                                &eval_cmd.expected,
                                eval_cmd.expect_ordered,
                                name,
                                eval_count,
                                &eval_cmd.query,
                            ) {
                                failures.push(e);
                            }
                        }
                        Err(e) => {
                            failures.push(format!(
                                "{} eval #{} (query: {}): {}",
                                name, eval_count, eval_cmd.query, e
                            ));
                        }
                    }
                }
            }
        }
    }

    if failures.is_empty() {
        Ok(())
    } else {
        Err(failures.join("\n"))
    }
}

/// Discover every `.test` file under `testdata/` (same directory the v1
/// harness reads).
fn discover_test_files() -> Vec<PathBuf> {
    let dir = Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("src")
        .join("promql")
        .join("promqltest")
        .join("testdata");

    let mut files: Vec<PathBuf> = std::fs::read_dir(&dir)
        .expect("failed to read testdata directory")
        .filter_map(|e| e.ok())
        .map(|e| e.path())
        .filter(|p| p.extension().and_then(|s| s.to_str()) == Some("test"))
        .collect();
    files.sort();
    files
}

/// Per-file v2 tests — one `#[tokio::test]` per fixture so failures land on a
/// specific file rather than a single aggregated failure. Generated by hand
/// because `build.rs` emits the v1 harness tests; extending `build.rs` would
/// touch shared infrastructure for a Phase-6-only purpose.
