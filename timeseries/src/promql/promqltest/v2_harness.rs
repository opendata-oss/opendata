    async fn run_fixture(stem: &str) -> Result<(), String> {
        let path = Path::new(env!("CARGO_MANIFEST_DIR"))
            .join("src")
            .join("promql")
            .join("promqltest")
            .join("testdata")
            .join(format!("{stem}.test"));
        let content = std::fs::read_to_string(&path)
            .map_err(|e| format!("failed to read fixture {stem}: {e}"))?;
        run_test_v2(stem, &content).await
    }

