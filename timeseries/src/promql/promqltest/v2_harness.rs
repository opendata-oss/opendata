    /// Verifies that the v2 harness itself is wired up correctly even when no
    /// fixture is present. This is a smoke test for the router — if
    /// `run_test_v2` itself can't evaluate a trivial load + eval pair, every
    /// fixture-level test above would fail for reasons unrelated to the
    /// engine, and this test isolates that.
    #[tokio::test]
    async fn should_route_trivial_fixture_through_v2() {
        // given
        let content = "\nload 5m\n  metric 1 2 3\n\neval instant at 10m\n  metric\n    {} 3\n";

        // when
        let result = run_test_v2("smoke", content).await;

        // then
        assert!(result.is_ok(), "smoke-test harness failed: {result:?}");
    }

    /// Smoke perf snapshot for RFC 0007 Phase 6 acceptance. Synthesises a
    /// counter metric with modest cardinality, runs
    /// `sum by (pod) (rate(http_requests_total[5m]))` over a 6h range via
    /// [`Tsdb::eval_query_range_v2`], reports median wall-time across 5
    /// iterations. Not a true criterion benchmark — the acceptance criterion
    /// only asks for a recorded number. Ignored by default so it doesn't
    /// slow the normal test run; invoke with
    /// `cargo test --features promql-v2 v2_harness_perf_smoke -- --ignored --nocapture`.
    #[tokio::test(flavor = "multi_thread", worker_threads = 2)]
    #[ignore]
    async fn v2_harness_perf_smoke() {
        use crate::model::QueryOptions;
        use std::time::{Duration, UNIX_EPOCH};

        // Synthesise a 100-pod × 3-instance counter: 300 series, 1 sample
        // per 30 s across 6 h → 720 samples per series, ~216k sample writes
        // in total. Small enough to populate quickly in the InMemoryStorage
        // test TSDB; large enough for the perf signal to be non-trivial.
        let tsdb = new_test_storage();
        let step_secs = 30u64;
        let range_secs = 6 * 3600u64;
        let num_steps = range_secs / step_secs;
        // 100 pods × 3 instances = 300 series — matches the RFC 0007
        // Phase 6 acceptance target now that 6.3.1 removed the
        // aggregate-panic cardinality cap that originally clipped this
        // to 30 series.
        let num_pods = 100usize;
        let num_instances = 3usize;

        let mut series: Vec<SeriesLoad> = Vec::with_capacity(num_pods * num_instances);
        for pod_idx in 0..num_pods {
            for inst_idx in 0..num_instances {
                let labels = std::collections::HashMap::from([
                    ("__name__".to_string(), "http_requests_total".to_string()),
                    ("pod".to_string(), format!("pod-{pod_idx}")),
                    ("instance".to_string(), format!("inst-{inst_idx}")),
                ]);
                let mut values = Vec::with_capacity(num_steps as usize);
                for step in 0..num_steps {
                    // Monotonically increasing counter, +1 per step.
                    values.push((step as i64, step as f64));
                }
                series.push(SeriesLoad { labels, values });
            }
        }
        load_series(&tsdb, Duration::from_secs(step_secs), &series)
            .await
            .expect("load_series failed");

        let query = "sum by (pod) (rate(http_requests_total[5m]))";
        let start = UNIX_EPOCH;
        let end = UNIX_EPOCH + Duration::from_secs(range_secs);
        let step = Duration::from_secs(step_secs);
        let opts = QueryOptions::default();

        // Warmup.
        let _ = tsdb
            .eval_query_range_v2(query, start..=end, step, &opts)
            .await
            .expect("warmup failed");

        // Measure 5 runs, take median.
        let mut samples_ms: Vec<u128> = Vec::with_capacity(5);
        for _ in 0..5 {
            let t0 = std::time::Instant::now();
            let r = tsdb
                .eval_query_range_v2(query, start..=end, step, &opts)
                .await
                .expect("range eval failed");
            let elapsed = t0.elapsed();
            samples_ms.push(elapsed.as_millis());
            // Ensure the output isn't optimised away.
            assert!(!r.into_matrix().is_empty(), "empty result");
        }
        samples_ms.sort();
        let median = samples_ms[samples_ms.len() / 2];
        eprintln!(
            "v2 perf smoke: {} steps × {} series ({} pods × {} instances): \
             median={} ms, samples={:?}",
            num_steps,
            num_pods * num_instances,
            num_pods,
            num_instances,
            median,
            samples_ms
        );
    }

    /// Surfaces every `.test` file we haven't hand-wired above. Fails loudly
    /// if a new fixture is added, so the v2 harness keeps up with the v1
    /// corpus without silent drift.
    #[test]
    fn should_cover_every_fixture_file() {
        let files = discover_test_files();
        let known: std::collections::HashSet<&str> = [
            "aggregators",
            "at_modifier",
            "functions",
            "name_label_dropping",
            "offset",
            "operators",
            "selectors",
            "subquery",
            "vector_matching",
        ]
        .into_iter()
        .collect();

        let missing: Vec<_> = files
            .iter()
            .filter_map(|p| p.file_stem().and_then(|s| s.to_str()))
            .filter(|stem| !known.contains(*stem))
            .map(|s| s.to_string())
            .collect();

        assert!(
            missing.is_empty(),
            "v2 harness missing entries for fixtures: {missing:?}"
        );
    }
